//Array In Typescript
//Array With Type Data Spesific

//Array With Type Data Number
let array: number[];
array = [1, 2, 3];

//Array With Type Data String
let array2: string[];
array2 = ["1", "2", "3"];

//Array With Type Data Any
let array3: any[];
array3 = [1, "string", true, {}];

//Tuples
//Array With More Than One Type Data Inside It and Spesific Length
let biodata: [string, number];
biodata = ["Aldo Ignata Chandra", 17];

let address: [string, number, boolean];
address = ["Surabaya", 13, true];
